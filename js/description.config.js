const makeTable = (td) => {
    const rowCnt = td[0].length; // 행 수
    const columnCnt = td.length  // 열 수

    let table = '';

    table += `
    <table class="property-grid-table"><tbody>
        <tr>
            <th>
                ${firstColTitle}
            </th>
            <th>
                ${secondColTitle}
            </th>
        </tr>
    `;

    for(let i = 0; i < rowCnt; i++){
        table += `<tr>`;
            for(let j = 0; j < columnCnt; j++){
                table += `<td>`;
                table += td[j][i];
                table += `</td>`;
            };
        table += `</tr>`;
    }
    table += `</tbody></table>`;

    return table;
};

