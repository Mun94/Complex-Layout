const splitDescription = (desc) => {
    const splitDesc = desc.split('\n');
    let includePTag = '';

    for(let i= 0; i < splitDesc.length; i++){
        includePTag += `<p>${splitDesc[i]}</p>`
    };

    return includePTag;
};

